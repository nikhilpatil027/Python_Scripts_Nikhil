mylist = [10, 19, 21, 80, 22, 47, 50, 70]
print("\n")
# 1) Print the list in Ascending order
ascending_order = sorted(mylist)
print("Ascending Order of my list:", ascending_order)

print("\n")

# 2) Print the list in Descending order
descending_order = sorted(mylist, reverse=True)
print("Descending Order of my list:", descending_order)

print("\n")
